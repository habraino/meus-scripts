public class OperadoresTeste {

    public static void main(String[] args) {
        
        int a = 1;
        while (a <= 10) {
            System.out.println(a);
            ++a;
        }

        System.out.println("======================================");
        int b = 10;
        while (b >= 1) {
            System.out.println(b);
            --b;
        }

        

    }

}